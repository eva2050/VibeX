class LogI18n {
  static get(key, lang = 'en', args = []) {
    if (!LogI18nDict[key]) return key;
    const template = LogI18nDict[key][lang] || LogI18nDict[key]['en'];
    if (!template) return key;

    let result = template;
    for (let i = 0; i < args.length; i++) {
      result = result.replace(`%{var${i}}`, args[i]);
    }
    return result;
  }
}
