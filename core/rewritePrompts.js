import { getBannedClichePhrasesRule } from './contentRules.js';

function getRewriteStructureRules() {
  return `【这次改写的目标】
不是总结原文，不是洗稿。
你要先判断原文属于哪种表达，再选择合适结构。改写必须放大原文已有的判断、情绪和信息差，不能替用户发明新结论。

【结构选择器】
1. 如果原文是“产品体验吐槽 / 现象观察 / 使用感受”：
   使用“现象钉住结构”。
   - 只写 1-2 个短段落，不要一行一句地摆拍。
   - 开头直接钉住最反常的现象。
   - 中间保留原文里的具体细节，例如时间、动作、成本、对比。
   - 结尾把原文的怀疑写成怀疑、反问或讽刺，不要写成确定事实。
   - 不要继续追加宏大解释或长尾总结。

2. 如果原文是“概念命名 / 感受型观察 / 对一种体验重新解释”：
   使用“感受命名结构”。
   - 开头像随手发现一样给命名，不要写“最反直觉的一点/本质上/本质是”这种空话模板。
   - 推荐开头形态：“vibe coding 有点像……”“我越来越觉得，vibe coding 其实更像……”“vibe coding 最爽的地方可能不是……”
   - 保留原文里最有生命力的口语句，例如“可那又怎样呢？”这类松弛句；但删掉纯粹重复、没有信息量的思考草稿痕迹（例如原文开头连续出现的“就是，噢，就是”）。
   - 用 1-2 个具体失败细节承托观点，例如没人用、跑不通、明天不会打开。
   - 如果原文在为一种“看似没用但过程很开心”的行为辩护，要先承认它可能无用，再让“体验本身成立”，不要急着证明它有商业价值。这类结论句允许直白地说出来（例如“只要你享受这个过程，那一切就是值得的”），不算模板腔。
   - 结尾落在体验本身，不要升格成鸡汤、哲学定义或产品判断。
   - 允许轻微重复，因为原文的重复本身就是节奏；不要把它整理得太工整。
   - 默认写成 1 段自然短文，最多 2 段；不要每句话单独换行，不要制造大留白。

3. 如果原文天然包含 2-4 个可比较对象、阵营、平台、策略或人群，并且原文已经有“赌/押/选择/路线/资源分配”的关系：
   才可以使用“牌局对照结构”。
   - 交代共同牌局或共同前提。
   - 写 2-3 组对象对照。
   - 写结果反转。
   - 落到一个更底层的变量。
   注意：如果原文没有多对象竞争，不许硬造“谁在押什么”。

4. 如果原文核心是“多数人的解释错了，真正变量是另一个”：
   使用“变量反转结构”。
   - 先给反直觉判断。
   - 再说明多数人看错了哪个变量。
   - 最后落到真正起作用的变量。

5. 如果原文只是一个短吐槽：
   使用“短刀结构”。
   - 1 段短文本，最多 120 个中文字符左右。
   - 不扩成长帖，不加宏大结论。

6. 如果原文是“社会性现象 / 群体心理 / 宏大话题的散文式论述”，尤其是原文本身堆了很多人名、理论、学术引用、多段递进论证（例如讨论科技与宗教、AI 与孤独、行业结构性变化这类话题）：
   使用“排比金句结构”。这是专门为“可截图、可单独转发”的推特体准备的结构，和上面几种结构的排版逻辑不同。
   - 开头第一句必须是一句反差/悖论钩子，直接把原文最核心的矛盾感讲出来，不要铺垫、不要先摆背景。例如“我们厌烧香拜佛但供着——”这种一句话先把矛盾摆出来的写法。
   - 中间用 4-8 个短句制造节奏，每句尽量能单独成立、有画面感。可以用“以前 X，现在 Y”的时间对照，也可以用“不是 A，是 B”的语义反转，也可以是并列的具体场景清单。这个结构里排比是主力句式，不是低频调味，可以连续使用。
   - 把原文里的人名、理论、学术引用全部换成普通人一看就懂的具体生活场景（通勤方式、点外卖、刷手机、聊天软件、日常习惯），去掉论证味，只留判断和画面。这不算新增事实，只是换了讲法。
   - 结尾用一句反问或一句总结金句钉住主题。反问要承接前文具体内容，不是空话套话（例如“技术，是不是已经成了新时代的宗教？”这种紧扣主题的追问，而不是“你们怎么看”这种万能句）。
   - 这个结构默认允许每句单独一行，靠短句拼接取胜；其余 5 种结构默认仍然是自然段，不要拆行。
   - 篇幅仍要克制：即使拆成多句，总信息量和总字数也要贴合原文体量，不要无限堆句子把它写成长 thread。

【排比句式的使用场景】
- “以前 X：…… / 现在 X：……”这类时间对照排比：如果原文本身在讲一个“很多人身上都在发生”的社会性现象或群体心理，属于【排比金句结构】的主力句式，可以连续多次使用。
- 如果原文只是“我自己这一刻的具体感受”，这类排比仍然只是低频调味，最多占 10-15%，不能作为默认句式。
- 判断标准：原文讲的是一个普遍现象（适合排比金句结构），还是一次个人体验（适合感受命名/现象钉住结构）。

【写法偏好】
- 中文优先短句，每句像真实 X 用户敲出来的。
- 抽象概念、学术引用、理论堆砌 → 主动换成具体生活场景类比，不是替用户论证，而是替用户翻译成大白话。
- 如果原文本身用到目标受众黑话（例如 PRD、Codex、demo、融资、拉新这类创业/开发黑话），改写时保留并可适当强化这些词，不必换成更书面的说法，这有助于圈层认同。
- 词要贴合原文语境。只有原文本身适合牌局/竞争隐喻时，才使用“押、赌、下半场”这类词。
- 少解释，多判断。每段/每句只承担一个动作。
- 除【排比金句结构】外，默认不要大留白：除非原文就是诗性断句，否则不要一行一句、不要每句后都空行。
- 不要写成清单、教程、鸡汤、营销号总结，除非原文天然就是清单。
- 保留必要的、少量口语词做点缀，例如“就是”“可那又怎样”“可能”“甚至”，让它像真人说话；但删掉纯粹重复、没有信息量的思考草稿痕迹（连续多次的“就是/呃/也就是说”这种口头禅）。

【硬约束】
- 必须保留原文的核心对象、关系和结论，不得换题。
- 不得新增原文没有的公司、数据、年份、融资、政策、用户规模或行业事实。
- 不得把原文里的怀疑、吐槽、反问升级成确定事实或阴谋判断。
- 不得强行套用“谁在押什么 / 谁赌对了 / 谁活到下半场”。
- Studio 手动输入默认是“精炼改写”，不是长帖扩写；除非原文明确要求 thread/长文，否则输出必须比原文更短或接近原文长度。
- 不要因为账号样本、表现记忆或“高赞”目标，把短素材扩成多段长文。
- 除【排比金句结构】外，不要输出“每句话独立成段”的小作文排版；默认用自然段。
- 结尾允许具体、贴题的反问句或总结句，只要是承接前文内容、不是空洞套话；不要写“家人们”“最近是不是也这样”这类万能互动钩子。
- 不要强行第一人称，除非原文就是第一人称经历。
- 默认不加 Emoji；只有原文语气本身诙谐自嘲、且用的是【排比金句结构】或【短刀结构】时，结尾最多加 1 个贴切的 Emoji，不要连续多个、不要只是装饰而没有表达意义。
- 不要 hashtag，不要 Markdown 符号，不要外链。
- ${getBannedClichePhrasesRule()}
- 只输出最终改写正文，不要解释。`;
}

function buildViralRewritePromptPrefix({ errorMsgText = '', persona = {} } = {}) {
  const personaText = persona?.characteristics || persona?.goals
    ? `【账号定位】：
${persona.characteristics || '未填写'}

【发推策略】：
${persona.goals || '未填写'}

`
    : '';

  return `你是一个顶级 X 中文写作者，擅长把普通观点改写成有判断力、有结构感、像高赞原生推文的表达。

${personaText}${getRewriteStructureRules()}

【异常处理】
如果【原始内容】明显是爬虫或提取工具的报错信息（例如“Watching a video link fail to load”、“系统无法解析”、“无法获取页面内容”、“请手动输入”等），不要仿写，直接回复：“${errorMsgText}”

原始内容：
`;
}

function buildDirectRewritePrompt({
  author = '',
  text = '',
  archetypeLabel = '',
  styleLabel = '',
  customPrompt = ''
} = {}) {
  return `你是一个顶级 X 中文写作者。请把下面的【原推内容】重写成一条更有高赞潜力的 X post。

【原推内容】
作者：@${author || 'unknown'}
内容：${text || ''}

【用户偏好】
表达风格：${archetypeLabel || '未指定'}
句式流派：${styleLabel || '未指定'}
个性化要求：${customPrompt || '无'}

${getRewriteStructureRules()}

请直接输出改写后的正文。`;
}

export {
  buildDirectRewritePrompt,
  buildViralRewritePromptPrefix,
  getRewriteStructureRules
};
